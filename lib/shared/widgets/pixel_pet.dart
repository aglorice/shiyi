import 'package:flutter/material.dart';

enum PixelPetType {
  cat,
  dog,
  rabbit,
  chick,
  bear,
  fox;

  String get label => switch (this) {
    cat => '小猫',
    dog => '小狗',
    rabbit => '小兔',
    chick => '小鸡',
    bear => '小熊',
    fox => '小狐',
  };

  static PixelPetType fromName(String? name) {
    if (name == null) return cat;
    for (final v in values) {
      if (v.name == name) return v;
    }
    return cat;
  }
}

class PixelPet extends StatelessWidget {
  const PixelPet({super.key, required this.type});

  final PixelPetType type;

  @override
  Widget build(BuildContext context) {
    return CustomPaint(
      size: const Size.square(72),
      painter: _PixelPetPainter(type),
    );
  }
}

